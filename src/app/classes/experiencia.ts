export class Experiencia {
    puesto:string;
    compania:string;
    fechaInicio:string;
    fechaFinal:string;

    constructor(puesto:string, compania:string, fechaInicio:string, fechaFinal:string){
        this.puesto = puesto;
        this.compania = compania;
        this.fechaInicio = fechaInicio;
        this.fechaFinal = fechaFinal;
    };
}
